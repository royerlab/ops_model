def build_bins(bin_size: int, top_max: int, bottom_max: int) -> list:
    """Decay-mode bins: sliding 100-cell windows across top + bottom."""
    bins = []
    for i in range(0, top_max, bin_size):
        bins.append((i + 1, min(i + bin_size, top_max), "top"))
    for i in range(0, bottom_max, bin_size):
        bins.append((i + 1, min(i + bin_size, bottom_max), "bottom"))
    return bins


# Default expand-mode window sizes: cumulative K (per gene). We expand
# top-K and bottom-K in parallel (same K values, both directions) so the
# two curves can be compared on the same axes.
DEFAULT_EXPAND_WINDOWS = [10, 50, 100, 500, 1000, 5000]


def build_sparse_bins(pma_csv: Path, window: int = 100) -> list:
    """Derive 100-cell bins from a sparse-rank PMA CSV.

    The sparse CSV contains the top-100 cells per gene at every
    1000-rank slice — ranks {1..100, 1001..1100, 2001..2100, …, K..K+99}.
    We bin into (k, k+window-1, "top") tuples by reading the unique
    starting ranks (rank % 1000 == 1) from the file.
    """
    p = _read_pma(pma_csv, columns=["rank"])
    # Each "slice" starts where rank ≡ 1 (mod 1000): 1, 1001, 2001, …
    starts = np.sort(p.loc[p["rank"] % 1000 == 1, "rank"].unique())
    return [(int(s), int(s + window - 1), "top") for s in starts]


def build_expand_bins(windows: list) -> list:
    """Expand-mode bins: cumulative (1..K) windows in BOTH directions.

    For each K we emit two bins: (1, K, "top") and (1, K, "bottom"). The
    bottom curve is the independent parallel of the top curve — same
    cumulative window applied to the bottom-K cells per gene — not a
    continuation of top.
    """
    bins = []
    for k in windows:
        bins.append((1, int(k), "top"))
        bins.append((1, int(k), "bottom"))
    return bins


# Expansion experiment: per-gene cumulative top-K bin sizes.
# Each K means "keep cells with local_rank ≤ K within their gene's
# all-cells PMA forward pass". The all-cells PMA assigns each cell exactly
# one rank under its own KO gene's classifier head, so K is also the max
# attention-ranked cells contributed per gene to this bin's mAP.
# Total cells ≈ K × N_genes minus spatial-NN match dropout (~5% at small K).
# Baseline (all cells) is drawn as a horizontal reference line on the plot.
DEFAULT_EXPANSION_BINS = [
    10, 100, 500, 1000, 5000, 7500, 10000,
    # Peak-densifying bins around K=15k (previous best for distinctiveness).
    12000, 13000, 14000, 15000, 16000, 17000, 18000,
    20000, 25000, 30000, 50000, 75000, 100000,
]
# CHAD's PMA caps at top-5000/gene; the "chad" + "mixed" directions don't
# extend past that.
CHAD_MAX_BIN = 5000


def build_expansion_bins(per_gene_sizes: list) -> list:
    """Per-gene cumulative top-K bins.

    Each K becomes a (1, K, "top") bin filter — the cells with the K
    highest attention scores within each gene's PMA forward pass.
    Only "top" since the all-cells PMA carries no rank_type="bottom".
    """
    return [(1, int(k), "top") for k in per_gene_sizes]


