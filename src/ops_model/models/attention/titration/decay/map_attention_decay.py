def _cell_hashes(experiment_arr, segmentation_arr) -> np.ndarray:
    """Deterministic uint64 hash per (experiment, segmentation) cell.

    Used by the "random" direction to globally sample exactly K cells
    per gene: orchestrator pre-computes a per-(gene, K) hash threshold,
    workers compute the same hash for cells they encounter and keep
    those with hash ≤ threshold. The K-th smallest hash per gene is
    exactly the threshold, so each gene contributes exactly K cells
    (no variance, unlike a coin-flip approach).

    Uses `pd.util.hash_array` (xxhash internally) for ~ns/cell speed.
    Stable across runs within the same pandas version, which is what we
    need since orchestrator + workers share the same env.
    """
    ids = (pd.Series(experiment_arr, dtype="object").astype(str) + "_"
           + pd.Series(segmentation_arr).astype("int64").astype(str))
    return pd.util.hash_array(ids.values)


def _read_chad_rank_for_exp(chad_pma_parquet: Path,
                              experiment: str) -> dict:
    """Per-cell CHAD rank lookup for one experiment.

    Returns dict {(pma_gene, segmentation): chad_rank} restricted to
    cells in CHAD's top-cohort (rank_type='top'). Only this experiment's
    rows are read via predicate pushdown so this stays fast (~ms per
    worker call vs ~30s scanning the full 75 MB CSV).
    """
    import pyarrow.parquet as papq
    t = papq.read_table(
        str(chad_pma_parquet),
        columns=["gene", "segmentation", "rank", "rank_type"],
        filters=[("experiment", "=", experiment),
                 ("rank_type", "=", "top")],
    )
    df = t.to_pandas()
    return {(str(g), int(s)): int(r)
            for g, s, r in zip(df["gene"], df["segmentation"], df["rank"])}


def _compute_compacted_thresholds(per_exp_dir: Path,
                                    bin_ks: list,
                                    chad_pma_parquet: Optional[Path] = None,
                                    chad_max_bin: int = 5000,
                                    intersection_percentile: int = 50) -> dict:
    """Per (gene, K) thresholds for EXACT K-cell selection in each
    direction (top / bottom / random), computed over CACHED cells only
    (i.e. cells that survived the spatial-NN match into the cell-dino
    h5ads). This compacts ranks within the cached subset so dropout
    doesn't make small bins miss cells.

    Returns a nested dict keyed by direction:
        {
          "top":    {(gene, K): max_pma_rank_for_exact_K},
          "bottom": {(gene, K): min_pma_rank_for_exact_K},
          "random": {(gene, K): max_uint64_hash_for_exact_K},
        }

    Workflow:
      1. Read each per-experiment cache's obs (just metadata, no X).
      2. Concat across experiments and exclude NTC rows.
      3. Per gene, sort cached cells three ways:
           - by attention rank ascending → top threshold = K-th rank
           - by attention rank descending → bottom threshold = K-th rank
           - by deterministic uint64 hash → random threshold = K-th hash
      4. Cells with PMA rank ≤ top_threshold[(G,K)] = exactly K cached cells.
         Same for the other directions with their respective comparator.

    Worker uses these thresholds to filter; result is exactly K cached
    cells per gene (regardless of spatial-NN dropout in the original PMA).
    """
    obs_blocks = []
    for p in sorted(per_exp_dir.glob("*.h5ad")):
        if p.stat().st_size == 0:
            continue
        try:
            ob = ad.read_h5ad(p, backed="r").obs
        except Exception:
            continue
        need = ["pma_gene", "pma_segmentation", "rank", "rank_type"]
        if not all(c in ob.columns for c in need):
            continue
        sub = ob[need].copy()
        sub["_exp"] = p.stem
        obs_blocks.append(sub)
    if not obs_blocks:
        raise RuntimeError(f"No usable per-experiment caches in {per_exp_dir}")

    df = pd.concat(obs_blocks, ignore_index=True)
    df = df[df["rank_type"].astype(str) == "top"]  # NTC has its own handling
    df["pma_gene"] = df["pma_gene"].astype(str)
    df["rank"] = pd.to_numeric(df["rank"], errors="coerce").astype("int64")
    df["_h"]    = _cell_hashes(df["_exp"].values,
                                df["pma_segmentation"].astype("int64").values)

    # Optional 4th direction: "mixed" = combined_rank top-K, where
    # combined_rank = min(geneKO_rank, chad_rank). Cells absent from
    # CHAD's PMA get chad_rank = MAX_INT, so combined_rank reduces to
    # geneKO_rank for them. We can only compute mixed thresholds for
    # bins ≤ chad_max_bin (CHAD's PMA only covers top-K under CHAD).
    if chad_pma_parquet is not None and chad_pma_parquet.exists():
        import pyarrow.parquet as papq
        ct = papq.read_table(
            str(chad_pma_parquet),
            columns=["gene", "experiment", "segmentation", "rank", "rank_type"],
            filters=[("rank_type", "=", "top")],
        )
        cdf = ct.to_pandas()
        cdf.rename(columns={"experiment": "_exp",
                              "segmentation": "pma_segmentation",
                              "gene": "pma_gene",
                              "rank": "chad_rank"}, inplace=True)
        cdf["pma_gene"] = cdf["pma_gene"].astype(str)
        cdf["pma_segmentation"] = cdf["pma_segmentation"].astype("int64")
        df = df.merge(
            cdf[["_exp", "pma_segmentation", "pma_gene", "chad_rank"]],
            on=["_exp", "pma_segmentation", "pma_gene"], how="left",
        )
        # Also merge CHAD's BOTTOM rows so we can compute the union-low
        # noise mask (random_low_removed_union direction). Cells in
        # CHAD's bottom-5k cohort are flagged as CHAD-low-attention;
        # union-low = geneKO-low OR CHAD-low.
        ct_bot = papq.read_table(
            str(chad_pma_parquet),
            columns=["gene", "experiment", "segmentation", "rank_type"],
            filters=[("rank_type", "=", "bottom")],
        ).to_pandas()
        ct_bot["in_chad_bottom"] = True
        ct_bot.rename(columns={"experiment": "_exp",
                                "segmentation": "pma_segmentation",
                                "gene": "pma_gene"}, inplace=True)
        ct_bot["pma_gene"] = ct_bot["pma_gene"].astype(str)
        ct_bot["pma_segmentation"] = ct_bot["pma_segmentation"].astype("int64")
        df = df.merge(
            ct_bot[["_exp", "pma_segmentation", "pma_gene", "in_chad_bottom"]],
            on=["_exp", "pma_segmentation", "pma_gene"], how="left",
        )
        df["in_chad_bottom"] = df["in_chad_bottom"].fillna(False).astype(bool)
        # Use 10**12 (1 trillion) as the "no CHAD match" sentinel —
        # safely larger than any real rank (max gene size <1M) yet
        # roundtrip-safe through float64. `np.iinfo(np.int64).max`
        # survives the float64 cast inconsistently: pandas .fillna() on
        # a float column → float64 → can't exactly represent INT64_MAX
        # → cast-back to int64 becomes INT64_MIN, which then poisons
        # every `combined_rank = min(real_rank, INT64_MIN)`.
        CHAD_MISSING_SENTINEL = 10**12
        df["chad_rank"] = (df["chad_rank"]
                             .fillna(CHAD_MISSING_SENTINEL).astype("int64"))
        df["combined_rank"] = np.minimum(
            df["rank"].astype("int64").values,
            df["chad_rank"].astype("int64").values,
        )

    out = {"top": {}, "bottom": {}, "random": {}, "mixed": {},
           "random_low_removed": {}, "random_low_removed_union": {},
           "random_low_removed_intersection": {},
           "upper_half_rank": {}}
    max_uint64 = int(np.iinfo(np.uint64).max)
    max_int64  = int(np.iinfo(np.int64).max)
    for gene, sub in df.groupby("pma_gene"):
        ranks_asc = np.sort(sub["rank"].values)
        ranks_desc = ranks_asc[::-1]
        hashes_asc = np.sort(sub["_h"].values)
        if "combined_rank" in sub.columns:
            mixed_asc = np.sort(sub["combined_rank"].values)
        else:
            mixed_asc = None
        n = len(ranks_asc)
        # "Upper" pool = top (1 - P/100) fraction of cached cells by
        # attention rank, where P = intersection_percentile (default 50 →
        # upper HALF, matching the original convention). Cells with rank
        # > upper_thresh are flagged "low" for the random_low_removed*
        # directions. Smaller P → smaller drop set; larger P → larger.
        n_upper = max(1, int(round(n * (1.0 - intersection_percentile / 100.0))))
        n_upper = min(n_upper, n)
        upper_half_thresh = int(ranks_asc[n_upper - 1])
        out["upper_half_rank"][gene] = upper_half_thresh
        # Hashes restricted to cells in the upper half.
        upper_mask = sub["rank"].values <= upper_half_thresh
        upper_h_sorted = np.sort(sub["_h"].values[upper_mask])
        n_pool = len(upper_h_sorted)
        # Union-low pool: cells in upper-half-geneKO AND NOT in CHAD's
        # bottom-5k. For genes CHAD doesn't cover, this equals the plain
        # upper-half pool (CHAD-bottom is always False) — falls back to
        # the same behavior as random_low_removed.
        if "in_chad_bottom" in sub.columns:
            union_mask = upper_mask & ~sub["in_chad_bottom"].values
        else:
            union_mask = upper_mask
        union_h_sorted = np.sort(sub["_h"].values[union_mask])
        n_union_pool = len(union_h_sorted)
        # Intersection-low pool: keep cell unless BOTH classifiers agree
        # it's low. = NOT (geneKO_low AND chad_low).
        # For genes CHAD doesn't cover: chad_low is False → intersection_low
        # is False → keep all cells (equivalent to pure random).
        # For CHAD-covered genes: only removes cells where geneKO_lower
        # AND chad_bottom-5k — much smaller removal than RLR. Tests the
        # "remove only true noise" hypothesis.
        if "in_chad_bottom" in sub.columns:
            inter_low = ~upper_mask & sub["in_chad_bottom"].values
            inter_mask = ~inter_low
        else:
            inter_mask = np.ones(len(sub), dtype=bool)
        inter_h_sorted = np.sort(sub["_h"].values[inter_mask])
        n_inter_pool = len(inter_h_sorted)
        for K in bin_ks:
            K = int(K)
            if K >= n:
                out["top"][(gene, K)]    = int(ranks_asc[-1])
                out["bottom"][(gene, K)] = int(ranks_asc[0])
                out["random"][(gene, K)] = max_uint64
            else:
                out["top"][(gene, K)]    = int(ranks_asc[K - 1])
                out["bottom"][(gene, K)] = int(ranks_desc[K - 1])
                out["random"][(gene, K)] = int(hashes_asc[K - 1])
            # random_low_removed: K-th smallest hash among upper-half
            # cells. When K ≥ n_pool we keep all upper-half cells (curve
            # plateaus at "upper-half aggregate" for large K).
            if K >= n_pool:
                out["random_low_removed"][(gene, K)] = max_uint64
            else:
                out["random_low_removed"][(gene, K)] = int(upper_h_sorted[K - 1])
            # Union-low: stricter pool (excludes both geneKO-low AND
            # CHAD-low). Smaller for CHAD-covered genes.
            if K >= n_union_pool:
                out["random_low_removed_union"][(gene, K)] = max_uint64
            else:
                out["random_low_removed_union"][(gene, K)] = int(union_h_sorted[K - 1])
            # Intersection-low: larger pool, removes only cells BOTH
            # classifiers call low. Equivalent to pure random for non-
            # CHAD genes.
            if K >= n_inter_pool:
                out["random_low_removed_intersection"][(gene, K)] = max_uint64
            else:
                out["random_low_removed_intersection"][(gene, K)] = int(inter_h_sorted[K - 1])
            # Mixed bins only meaningful up to chad_max_bin (where CHAD's
            # ranks exist). Past that, combined_rank ≈ geneKO_rank, so
            # mixed ≈ top — not interesting. Cap to keep plot honest.
            if mixed_asc is not None and K <= chad_max_bin:
                if K >= n:
                    out["mixed"][(gene, K)] = max_int64
                else:
                    out["mixed"][(gene, K)] = int(mixed_asc[K - 1])
    return out


def _read_pma(pma_path: Path,
               columns: Optional[list] = None,
               experiment: Optional[str] = None) -> pd.DataFrame:
    """Read a PMA table (CSV or parquet), optionally column-pruned and/or
    filtered to one experiment via pyarrow predicate pushdown.

    Parquet form is hugely faster for the 60M-row all-cells tables since
    pyarrow can skip non-matching row groups + column-prune in one pass.
    CSV path is the legacy fallback for the v3 top/bottom CSVs.
    """
    p = str(pma_path)
    if p.endswith(".parquet"):
        import pyarrow.parquet as papq
        filters = [("experiment", "=", experiment)] if experiment else None
        return papq.read_table(p, columns=columns, filters=filters).to_pandas()
    df = pd.read_csv(p, usecols=columns)
    if experiment is not None:
        df = df[df["experiment"].astype(str) == experiment].copy()
    return df


def _load_pca(pca_h5ad: Path) -> Tuple[np.ndarray, list]:
    """Return (components (97×1024), feature_names (1024,)).

    pca_optimization stores these in `Phase_gene.h5ad.uns`. The model
    expects z-scored input per pca_optimization/phase1.py (zscore_per_exp
    StandardScaler applied per experiment before fit).
    """
    a = ad.read_h5ad(pca_h5ad, backed="r")
    try:
        comp = np.asarray(a.uns["pca_components"], dtype=np.float32)
        fnames = [str(s) for s in a.uns["pca_feature_names"]]
    finally:
        a.file.close()
    return comp, fnames


def _per_exp_zscore_factors(cell_h5ad: Path,
                              chunk: int = 200_000) -> Tuple[np.ndarray, np.ndarray]:
    """Stream-compute per-feature mean + std across ALL cells in the
    experiment's cell-dino h5ad.

    Matches pca_optimization's StandardScaler.fit_transform applied per
    experiment. Streaming avoids materializing the full ~4 GB matrix.
    Returns (mean (n_feats,), std (n_feats,) clipped to ≥ 1e-8).
    """
    a = ad.read_h5ad(cell_h5ad, backed="r")
    try:
        n, d = a.n_obs, a.n_vars
        sumX  = np.zeros(d, dtype=np.float64)
        sumX2 = np.zeros(d, dtype=np.float64)
        for s in range(0, n, chunk):
            e = min(s + chunk, n)
            X = np.asarray(a.X[s:e], dtype=np.float64)
            sumX  += X.sum(axis=0)
            sumX2 += (X * X).sum(axis=0)
        mean = sumX / max(n, 1)
        var = np.clip(sumX2 / max(n, 1) - mean * mean, 0, None)
        std = np.sqrt(var).clip(1e-8)
        return mean.astype(np.float32), std.astype(np.float32)
    finally:
        a.file.close()


def _project_pca(X: np.ndarray,
                  mean: np.ndarray, std: np.ndarray,
                  components: np.ndarray) -> np.ndarray:
    """Z-score per feature then project to PCA space.

    `X` is (n_cells × 1024) raw cell-dino. Per pca_optimization, the
    StandardScaler is fit per experiment, so `mean`/`std` come from
    that experiment's full cell population. After z-score the data is
    zero-centered, so PCA projection is just X_z @ components.T.
    """
    X_z = (X.astype(np.float32) - mean) / std
    return X_z @ components.T


# ── Build / load filtered per-cell cache ─────────────────────────────────
def _has_ntc_rows(h5ad_path: Path) -> bool:
    """Quick check: does this per-experiment cache include NTC cells?

    Older caches (built before NTC normalization was wired in) lack
    NTC rows; we use this as a freshness flag to trigger rebuilds.
    """
    try:
        import h5py
        with h5py.File(h5ad_path, "r") as f:
            # obs is an h5 group; rank_type stored as categorical or
            # str depending on writer.
            obs = f.get("obs")
            if obs is None:
                return False
            rt = obs.get("rank_type")
            if rt is None:
                return False
            # Categorical: codes + categories. Plain: dataset of bytes.
            if isinstance(rt, h5py.Group):
                cats = rt.get("categories")
                if cats is None:
                    return False
                vals = [s.decode() if isinstance(s, bytes) else str(s)
                        for s in cats[:]]
                return any(v.upper() == "NTC" for v in vals)
            # Dataset: bytes/str column. Read up to first 5000 entries.
            sample = rt[:5000] if rt.shape and rt.shape[0] > 5000 else rt[:]
            vals = [s.decode() if isinstance(s, bytes) else str(s)
                    for s in sample]
            return any(v.upper() == "NTC" for v in vals)
    except Exception:
        return False


def _flatten_well(w) -> str:
    """Normalize well to 'A1' style for join compatibility.

    Cell-dino obs stores well as e.g. 'A/2/0_ops0031_20250424' (well +
    experiment tag); PMA uses 'A2'. Strip everything after the first
    '_' (the experiment tag), then drop slashes.
    """
    s = str(w).split("_")[0]
    if "/" in s:
        parts = s.split("/")
        return f"{parts[0]}{parts[1]}" if len(parts) >= 2 else s
    return s


def _filter_one_experiment(
    exp: str,
    pma_csv_s: str,
    out_path_s: str,
    channel: str = PHASE_CHANNEL,
    feature_dir: str = FEATURE_DIR,
    match_radius_px: float = 5.0,
    pca_h5ad_s: Optional[str] = None,
    out_path_pca_s: Optional[str] = None,
    strict_own_ko: bool = False,
    apply_iss_sidecar: bool = False,
    pca_cache_only: bool = False,
) -> str:
    """SLURM worker: load one experiment's cell-dino features, spatial-NN
    match to PMA, write a per-experiment filtered h5ad.

    Top-level (picklable) for submitit. Reads only the rows of PMA for
    this experiment + this experiment's cell-dino h5ad.
    """
    from scipy.spatial import cKDTree
    from ops_utils.data.feature_discovery import (
        get_channel_maps_path, get_storage_roots, load_cell_h5ad,
    )

    out_path = Path(out_path_s)
    pca_out = Path(out_path_pca_s) if out_path_pca_s else None
    raw_done = out_path.exists() and _has_ntc_rows(out_path)
    pca_done = (pca_out is None) or (pca_out.exists() and _has_ntc_rows(pca_out))
    # When pca_cache_only, the raw 1024-d cache is intentionally skipped —
    # downstream bin scoring uses pca_only=True and _compute_compacted_thresholds
    # is pointed at the PCA dir (obs is identical). Treat raw as "done".
    if pca_cache_only:
        raw_done = True
    if raw_done and pca_done:
        return f"{exp}: skipped (exists)"

    pma = _read_pma(
        Path(pma_csv_s),
        columns=["experiment", "well", "segmentation", "gene",
                 "x_pheno", "y_pheno", "rank", "rank_type"],
        experiment=exp,
    )
    if pma.empty:
        return f"{exp}: no PMA rows"
    pma["well_flat"] = pma["well"].map(_flatten_well)

    storage_roots = get_storage_roots()
    metadata_path = get_channel_maps_path()
    # Track the resolved h5ad path so the PCA pass below can re-open it
    # to compute per-experiment z-score factors (full-population mean +
    # std, streaming).
    from ops_utils.data.feature_discovery import find_cell_h5ad_path
    cell_path = find_cell_h5ad_path(exp, channel, storage_roots,
                                      feature_dir, metadata_path)
    if cell_path is None:
        return f"{exp}: no h5ad"
    if apply_iss_sidecar:
        # Sidecar-corrected obs[perturbation]/[sgRNA] reflect the current
        # 3-assembly linked_pheno_iss.csv calls (not the frozen snapshot
        # the data_loader defaults to). Orphans (cells the ISS re-run
        # dropped entirely) are excluded — they have no current call.
        from ops_model.features.anndata_utils import load_features_corrected
        a = load_features_corrected(cell_path, drop_orphans=True)
    else:
        a = ad.read_h5ad(cell_path)
    if a is None:
        return f"{exp}: no h5ad"

    obs = a.obs.copy()
    obs["well_flat"] = obs["well"].map(_flatten_well)
    obs["_row"] = np.arange(len(obs), dtype=np.int64)

    matched_rows = []
    matched_pma = []
    for well, pma_w in pma.groupby("well_flat"):
        obs_w = obs[obs["well_flat"] == well]
        if obs_w.empty or pma_w.empty:
            continue
        tree = cKDTree(obs_w[["x_position", "y_position"]].values)
        dists, idxs = tree.query(
            pma_w[["x_pheno", "y_pheno"]].values, k=1,
        )
        keep = dists < match_radius_px
        if not keep.any():
            continue
        matched_rows.append(obs_w["_row"].values[idxs[keep]])
        mp = pma_w.loc[keep, ["rank", "rank_type", "gene", "segmentation"]].copy()
        mp["match_dist"] = dists[keep]
        matched_pma.append(mp)

    if not matched_rows:
        out_path.touch()
        return f"{exp}: 0 cells matched"

    keep_rows = np.concatenate(matched_rows)
    meta_df = pd.concat(matched_pma, ignore_index=True)
    # All-cells PMAs score every cell against every classifier head, so
    # each physical cell shows up under many `gene` labels — without the
    # preference below, dedup picks one essentially at random and the
    # cell's labelled gene won't be its true KO. Prefer the PMA row where
    # the PMA `gene` matches the cell's actual `perturbation` so each
    # cell keeps its own-KO rank. Falls back to closest-match-dist for
    # cells with no own-KO PMA row (e.g. NTC sneaking through, or genes
    # not in the classifier's vocab).
    cell_perturbation = obs["perturbation"].astype(str).to_numpy()[keep_rows]
    meta_df["_keep_row"] = keep_rows
    meta_df["_own_ko"] = (
        meta_df["gene"].astype(str).values == cell_perturbation
    ).astype(np.int8)
    meta_df = meta_df.sort_values(
        by=["_own_ko", "match_dist"], ascending=[False, True],
    ).reset_index(drop=True)
    keep_rows = meta_df["_keep_row"].to_numpy()
    _, uniq_idx = np.unique(keep_rows, return_index=True)
    keep_rows = keep_rows[uniq_idx]
    meta_df = meta_df.iloc[uniq_idx].reset_index(drop=True)
    # Strict mode: ONLY keep cells whose PMA `gene` exactly matches the
    # cell's own perturbation. Drops the ~20% cross-talk cells where
    # spatial-NN attached a PMA row from a different gene (the cell's
    # own-KO PMA row wasn't within 5px or didn't survive dedup). Yields
    # a smaller but provably-clean cache where pma_gene == perturbation
    # for every row, so the bin filter's gene labels match true KO.
    if strict_own_ko:
        n_before = len(meta_df)
        own_ko_mask = meta_df["_own_ko"].astype(bool).to_numpy()
        keep_rows = keep_rows[own_ko_mask]
        meta_df = meta_df[own_ko_mask].reset_index(drop=True)
        print(f"{exp}: strict-mode dropped {n_before - len(meta_df):,}"
              f"/{n_before:,} cells with pma_gene != perturbation "
              f"({100*(n_before - len(meta_df))/max(n_before,1):.1f}%)")
    meta_df = meta_df.drop(columns=["_keep_row", "_own_ko"])

    # Also grab ALL NTC cells from this experiment, regardless of PMA
    # rank. We carry them through every bin so the gene-level
    # NTC-norm step (matching pca_optimization's `normalize_guide_adata`
    # call) has reference cells.
    ntc_mask = obs["perturbation"].astype(str).str.upper() == "NTC"
    ntc_idx = obs.loc[ntc_mask, "_row"].to_numpy(dtype=np.int64)
    # Skip rows already matched by PMA (shouldn't happen for NTC, but
    # belt and suspenders to avoid duplicates).
    if len(ntc_idx) > 0:
        ntc_idx = np.setdiff1d(ntc_idx, keep_rows, assume_unique=False)

    all_rows = np.concatenate([keep_rows, ntc_idx]) if len(ntc_idx) else keep_rows
    sub = a[all_rows].copy()
    n_pma = len(keep_rows)
    n_ntc = len(ntc_idx)
    # Sentinel rank values for NTC cells: rank_type="NTC", rank=-1.
    rank_arr      = np.concatenate([meta_df["rank"].to_numpy(),
                                    np.full(n_ntc, -1, dtype=np.int64)])
    rank_type_arr = np.concatenate([meta_df["rank_type"].to_numpy(),
                                    np.full(n_ntc, "NTC", dtype=object)])
    gene_arr      = np.concatenate([meta_df["gene"].to_numpy(),
                                    np.full(n_ntc, "NTC", dtype=object)])
    seg_arr       = np.concatenate([meta_df["segmentation"].to_numpy(),
                                    np.full(n_ntc, -1, dtype=np.int64)])
    sub.obs["rank"] = rank_arr
    sub.obs["rank_type"] = rank_type_arr
    sub.obs["pma_gene"] = gene_arr
    sub.obs["pma_segmentation"] = seg_arr
    if not raw_done:
        out_path.parent.mkdir(parents=True, exist_ok=True)
        tmp = out_path.with_suffix(".h5ad.tmp")
        sub.write_h5ad(tmp)
        tmp.replace(out_path)

    # PCA-projected sibling — same per-experiment cells, projected
    # into pca_optimization's 97-dim phase-only PCA space. The PCA
    # was fit on z-scored-per-experiment data, so we replicate that
    # exact preprocessing: stream-compute (mean, std) on the full
    # experiment's cell-dino X, then z-score + project the matched
    # cells. Written to `out_path_pca_s` as a parallel cache.
    if pca_h5ad_s and out_path_pca_s:
        pca_out = Path(out_path_pca_s)
        if pca_out.exists():
            pass
        else:
            comp, _fnames = _load_pca(Path(pca_h5ad_s))
            mean, std = _per_exp_zscore_factors(cell_path)
            # `a` is already in-memory from above; matched cell X is a
            # small slice of it. No need to re-open. Use all_rows so
            # NTC cells get PCA-projected too.
            X_matched = np.asarray(a[all_rows].X, dtype=np.float32)
            X_pcs = _project_pca(X_matched, mean, std, comp)
            obs_pca = sub.obs.copy().reset_index(drop=True)
            sub_pca = ad.AnnData(
                X=X_pcs.astype(np.float32),
                obs=obs_pca,
                var=pd.DataFrame(index=[f"PC{i}" for i in range(X_pcs.shape[1])]),
            )
            tmp = pca_out.with_suffix(".h5ad.tmp")
            sub_pca.write_h5ad(tmp)
            tmp.replace(pca_out)

    return f"{exp}: matched {sub.n_obs:,}/{len(pma):,} = {100*sub.n_obs/len(pma):.1f}%"


def build_filtered_cache(
    pma_csv: Path,
    cache_path: Path,
    channel: str = PHASE_CHANNEL,
    feature_dir: str = FEATURE_DIR,
    match_radius_px: float = 5.0,
    use_slurm: bool = False,
    slurm_params: Optional[dict] = None,
    skip_final_concat: bool = False,
    pca_h5ad: Optional[Path] = None,
    raw_subdir_name: str = "per_experiment_filtered",
    pca_subdir_name: str = "per_experiment_filtered_pca",
    job_tag: str = "filter",
    wait_for_completion: bool = True,
    strict_own_ko: bool = False,
    apply_iss_sidecar: bool = False,
    pca_cache_only: bool = False,
) -> Optional[ad.AnnData] | dict:
    """Per-experiment per-well spatial nearest-neighbor join PMA → cell-dino.

    cell-dino h5ads don't carry a segmentation_id column, so we can't do
    the exact-seg match `consolidate_top_attention_cells.py` uses for OP.
    Instead, KD-tree spatial NN on (x_pheno, y_pheno) → (x_position,
    y_position), per (experiment, well_flat). Cells whose nearest
    cell-dino neighbor is farther than `match_radius_px` are dropped —
    accept ~80% recovery in exchange for clean per-cell rank labels.
    """
    from scipy.spatial import cKDTree
    from ops_utils.data.feature_discovery import (
        get_channel_maps_path, get_storage_roots, load_cell_h5ad,
    )

    print(f"[cache build] Reading PMA: {pma_csv}")
    pma = _read_pma(pma_csv, columns=["experiment"])
    experiments = sorted(pma["experiment"].astype(str).unique().tolist())
    print(f"  experiments to process: {len(experiments)}")

    # Per-experiment filtered h5ad dir (one file per experiment).
    per_exp_dir = cache_path.parent / raw_subdir_name
    per_exp_dir.mkdir(parents=True, exist_ok=True)
    per_exp_pca_dir = cache_path.parent / pca_subdir_name
    if pca_h5ad is not None:
        per_exp_pca_dir.mkdir(parents=True, exist_ok=True)

    def _missing(exp: str) -> bool:
        if not pca_cache_only:
            raw_p = per_exp_dir / f"{exp}.h5ad"
            if not raw_p.exists() or not _has_ntc_rows(raw_p):
                return True
        if pca_h5ad is not None:
            pca_p = per_exp_pca_dir / f"{exp}.h5ad"
            if not pca_p.exists() or not _has_ntc_rows(pca_p):
                return True
        return False

    if use_slurm:
        print(f"[cache build] Fanning {len(experiments)} experiments out to SLURM…")
        from ops_utils.hpc.slurm_batch_utils import submit_parallel_jobs
        jobs = [
            {
                "name": f"exp_{exp}",
                "func": _filter_one_experiment,
                "kwargs": dict(
                    exp=exp,
                    pma_csv_s=str(pma_csv),
                    out_path_s=str(per_exp_dir / f"{exp}.h5ad"),
                    channel=channel,
                    feature_dir=feature_dir,
                    match_radius_px=match_radius_px,
                    pca_h5ad_s=(str(pca_h5ad) if pca_h5ad is not None else None),
                    out_path_pca_s=(str(per_exp_pca_dir / f"{exp}.h5ad")
                                     if pca_h5ad is not None else None),
                    strict_own_ko=strict_own_ko,
                    apply_iss_sidecar=apply_iss_sidecar,
                    pca_cache_only=pca_cache_only,
                ),
                "metadata": {"experiment": exp},
            }
            for exp in experiments
            if _missing(exp)
        ]
        n_skip = len(experiments) - len(jobs)
        print(f"  submitting {len(jobs)} ({n_skip} already done)")
        if jobs:
            result = submit_parallel_jobs(
                jobs_to_submit=jobs,
                experiment=f"map_attention_decay_{job_tag}",
                slurm_params=slurm_params or {
                    "timeout_min": 30,
                    "slurm_mem":   "32GB",
                    "cpus_per_task": 4,
                    "slurm_partition": "cpu",
                },
                log_dir=f"map_attention_decay_{job_tag}",
                manifest_prefix=f"map_attention_decay_{job_tag}",
                wait_for_completion=wait_for_completion,
            )
            # When the caller wants to wait across multiple cache arrays
            # (e.g. expansion mode runs fast + slow in parallel), return
            # the array info so the caller can pass it to
            # `wait_for_multiple_job_arrays`. None when no jobs were
            # submitted (everything already cached).
            if not wait_for_completion:
                return result
        elif not wait_for_completion:
            return None
    else:
        # Inline (sequential) — same logic, no SLURM.
        for i, exp in enumerate(experiments, 1):
            out_p = per_exp_dir / f"{exp}.h5ad"
            if not _missing(exp):
                print(f"  [{i}/{len(experiments)}] {exp}: cached")
                continue
            msg = _filter_one_experiment(
                exp=exp, pma_csv_s=str(pma_csv), out_path_s=str(out_p),
                channel=channel, feature_dir=feature_dir,
                match_radius_px=match_radius_px,
                pca_h5ad_s=(str(pca_h5ad) if pca_h5ad is not None else None),
                out_path_pca_s=(str(per_exp_pca_dir / f"{exp}.h5ad")
                                  if pca_h5ad is not None else None),
                strict_own_ko=strict_own_ko,
                apply_iss_sidecar=apply_iss_sidecar,
                pca_cache_only=pca_cache_only,
            )
            print(f"  [{i}/{len(experiments)}] {msg}")

    if skip_final_concat:
        # Bin workers read the per-experiment files directly — skip the
        # slow 1.6M-row concat + write.
        print(f"\n[cache build] Per-experiment cache ready at {per_exp_dir} "
              f"(skipped final concat).")
        return None

    # Concatenate all per-experiment filtered h5ads.
    print(f"\n[cache build] Concatenating per-experiment filtered h5ads…")
    blocks = []
    total_pma = len(_read_pma(pma_csv, columns=["experiment"]))
    for exp in experiments:
        p = per_exp_dir / f"{exp}.h5ad"
        if not p.exists() or p.stat().st_size == 0:
            continue
        try:
            blocks.append(ad.read_h5ad(p))
        except Exception as e:
            print(f"  warn: failed to read {p}: {e}")

    if not blocks:
        raise SystemExit("No per-experiment filtered h5ads — SLURM array may have failed.")

    merged = ad.concat(blocks, join="outer", merge="same", label="block_experiment")
    recovery = 100 * merged.n_obs / max(1, total_pma)
    print(f"  total: {merged.n_obs:,} cells × {merged.n_vars} features "
          f"({recovery:.1f}% PMA recovery)")
    print(f"  rank_type counts: {pd.Series(merged.obs['rank_type']).value_counts().to_dict()}")
    cache_path.parent.mkdir(parents=True, exist_ok=True)
    print(f"[cache build] Writing to {cache_path}")
    merged.write_h5ad(cache_path)
    return merged


def load_or_build_cache(
    pma_csv: Path, cache_path: Path,
    use_slurm: bool = False, slurm_params: Optional[dict] = None,
) -> ad.AnnData:
    if cache_path.exists():
        print(f"Loading cached filtered cell-dino h5ad: {cache_path}")
        return ad.read_h5ad(cache_path)
    return build_filtered_cache(
        pma_csv, cache_path,
        use_slurm=use_slurm, slurm_params=slurm_params,
    )


# ── Per-bin scoring ──────────────────────────────────────────────────────
def _build_local_rank_offsets(pma_csv: Path) -> dict:
    """Per-gene-per-rank_type minimum rank (offset). Bin filtering uses
    `local_rank = rank - offset + 1` so each rank_type's per-gene 5000
    cells get a clean 1..5000 numbering.

    For top: per-gene min is 1 (rank is already per-gene-from-1).
    For bottom: per-gene min varies (e.g. AACS=63757) — this is the
    gene-specific PMA pipeline offset that broke our naive `rank<=100`
    filter for bottom bins.
    """
    p = _read_pma(pma_csv, columns=["gene", "rank", "rank_type"])
    p["gene"] = p["gene"].astype(str)
    mins = p.groupby(["rank_type", "gene"])["rank"].min().to_dict()
    return mins


def compute_bin_map_from_per_exp(
    per_exp_dir: Path, lo: int, hi: int, rank_type: str,
    rank_offsets: dict,
    null_size: int = 10_000,
    distrib_csv: Optional[Path] = None,
    chad_yaml: Optional[Path] = None,
    ntc_per_exp: int = 1500,
    gene_max_ranks: Optional[dict] = None,
    compacted_thresholds: Optional[dict] = None,
    chad_pma_parquet: Optional[Path] = None,
) -> Optional[dict]:
    """Read each per-experiment filtered h5ad with raw h5py row indexing
    (no anndata slicing) so we materialize ONLY the bin-matching rows.

    `rank_type` semantics:
      - "top"     : cells with local_rank in [lo, hi] under their gene's
                    own attention forward pass (existing behavior).
      - "bottom"  : same K-per-gene but from the OPPOSITE end of the
                    ranking — needs `gene_max_ranks` to invert. The bin
                    keeps cells with (max_rank - rank + 1) in [lo, hi].
      - "random"  : random sample of `hi` cells per gene, ignoring
                    attention rank entirely. `lo` is unused for random.
                    Deterministic per-experiment via seed=(name, hi).
      - "top+bottom": legacy expand-mode mix (not used by expansion).

    Direct h5py fancy indexing on the X dataset bounds memory to
    (n_bin_cells × n_features × 4 bytes).
    """
    import h5py

    X_blocks, obs_blocks, var_names = [], [], None
    for p in sorted(per_exp_dir.glob("*.h5ad")):
        if p.stat().st_size == 0:
            continue
        try:
            # Read obs cheaply (tiny — ~1k rows × few columns).
            obs = ad.read_h5ad(p, backed="r").obs.copy()
        except Exception:
            continue
        try:
            # Compute per-gene local rank within rank_type. Bottom cells
            # have gene-specific PMA offsets — a naive `rank>=lo & rank<=hi`
            # filter misses them entirely (their `rank` starts at ~5418+).
            obs_rt = obs["rank_type"].astype(str)
            obs_g  = obs.get("pma_gene", obs.get("perturbation")).astype(str)
            obs_r  = pd.to_numeric(obs["rank"], errors="coerce").astype("Int64")
            # Look up offset per (rank_type, gene), default 1 (no offset).
            offset = pd.Series(
                [int(rank_offsets.get(("top", g), 1)) for g in obs_g.values],
                index=obs.index, dtype="Int64",
            )
            local_rank = obs_r - offset + 1
            if rank_type == "top+bottom":
                top_cap = min(hi, 5000)
                bot_cap = max(0, hi - 5000)
                rank_mask = (
                    ((obs_rt == "top") & (local_rank >= 1) & (local_rank <= top_cap))
                    | ((obs_rt == "bottom") & (local_rank >= 1) & (local_rank <= bot_cap))
                )
            elif rank_type in ("top", "bottom", "random", "mixed",
                                 "random_low_removed",
                                 "random_low_removed_union",
                                 "random_low_removed_intersection") and compacted_thresholds:
                # EXACT K cells per gene via pre-computed thresholds over
                # CACHED cells (not raw PMA). Spatial-NN match dropout
                # is already accounted for: thresholds were derived from
                # the cached subset, so cells passing the threshold are
                # exactly K per gene regardless of which cells fell out
                # of the cache. Same logic for all 3 directions, just
                # different comparator + threshold source.
                K_int = int(hi)
                obs_g_str = obs_g.astype(str)
                d_thresh = compacted_thresholds.get(rank_type, {})
                if rank_type == "top":
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("int64").values
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(obs_r.astype("int64").values <= thresh,
                                      index=obs.index))
                    )
                elif rank_type == "bottom":
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get(
                            (g, K_int), np.iinfo(np.int64).max))
                    ).astype("int64").values
                    rank_mask = (
                        (obs_rt == "top")  # cache stores PMA rank_type='top' only
                        & (pd.Series(obs_r.astype("int64").values >= thresh,
                                      index=obs.index))
                    )
                elif rank_type == "random":
                    exp_name = p.stem
                    seg_arr = obs.get("pma_segmentation",
                                       obs.get("segmentation"))
                    if seg_arr is None:
                        raise ValueError(
                            "obs missing pma_segmentation/segmentation column"
                        )
                    cell_h = _cell_hashes(
                        np.full(len(obs), exp_name, dtype=object),
                        seg_arr.astype("int64").values,
                    )
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("uint64").values
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(cell_h <= thresh, index=obs.index))
                    )
                elif rank_type == "random_low_removed":
                    # Drop bottom 50% by attention, random sample K from
                    # remainder. Tests whether the model's value is
                    # mostly in pruning bad cells (here filtered) vs
                    # picking the very best (top direction).
                    upper_lookup = compacted_thresholds.get("upper_half_rank", {})
                    upper_thresh = obs_g_str.map(
                        lambda g: int(upper_lookup.get(g, 0))
                    ).astype("int64").values
                    in_upper = obs_r.astype("int64").values <= upper_thresh
                    exp_name = p.stem
                    seg_arr = obs.get("pma_segmentation",
                                       obs.get("segmentation"))
                    cell_h = _cell_hashes(
                        np.full(len(obs), exp_name, dtype=object),
                        seg_arr.astype("int64").values,
                    )
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("uint64").values
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(in_upper & (cell_h <= thresh),
                                       index=obs.index))
                    )
                elif rank_type == "random_low_removed_intersection":
                    # Drop ONLY cells where BOTH classifiers agree are
                    # low (geneKO_lower AND chad_bottom). Pool excludes
                    # this intersection; samples K random from rest.
                    # For genes CHAD doesn't cover, intersection is
                    # empty → pool = all cells → equivalent to pure
                    # random sampling. Useful as a stricter "noise"
                    # filter than RLR — only removes consensus noise.
                    if chad_pma_parquet is None:
                        raise ValueError(
                            "random_low_removed_intersection requires "
                            "chad_pma_parquet"
                        )
                    upper_lookup = compacted_thresholds.get("upper_half_rank", {})
                    upper_thresh = obs_g_str.map(
                        lambda g: int(upper_lookup.get(g, 0))
                    ).astype("int64").values
                    in_lower = obs_r.astype("int64").values > upper_thresh
                    import pyarrow.parquet as papq
                    exp_name = p.stem
                    ct = papq.read_table(
                        str(chad_pma_parquet),
                        columns=["gene", "segmentation", "rank_type"],
                        filters=[("experiment", "=", exp_name),
                                  ("rank_type", "=", "bottom")],
                    ).to_pandas()
                    chad_bot_set = {(str(g), int(s)) for g, s
                                      in zip(ct["gene"], ct["segmentation"])}
                    seg_arr = obs.get("pma_segmentation",
                                       obs.get("segmentation")).astype("int64").values
                    in_chad_bot = np.fromiter(
                        ((g, int(s)) in chad_bot_set
                         for g, s in zip(obs_g_str.values, seg_arr)),
                        dtype=bool, count=len(obs),
                    )
                    in_intersection_low = in_lower & in_chad_bot
                    cell_h = _cell_hashes(
                        np.full(len(obs), exp_name, dtype=object),
                        seg_arr,
                    )
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("uint64").values
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(
                            (~in_intersection_low) & (cell_h <= thresh),
                            index=obs.index,
                        ))
                    )
                elif rank_type == "random_low_removed_union":
                    # Like random_low_removed but ALSO excludes cells in
                    # CHAD's bottom-5k cohort. Pool = upper-half-geneKO
                    # AND NOT-in-CHAD-bottom. For genes CHAD doesn't
                    # cover, equivalent to random_low_removed (the CHAD
                    # mask is empty for those rows).
                    if chad_pma_parquet is None:
                        raise ValueError(
                            "random_low_removed_union direction requires "
                            "chad_pma_parquet"
                        )
                    upper_lookup = compacted_thresholds.get("upper_half_rank", {})
                    upper_thresh = obs_g_str.map(
                        lambda g: int(upper_lookup.get(g, 0))
                    ).astype("int64").values
                    in_upper = obs_r.astype("int64").values <= upper_thresh
                    # Build CHAD-bottom (gene, seg) set for this experiment.
                    import pyarrow.parquet as papq
                    exp_name = p.stem
                    ct = papq.read_table(
                        str(chad_pma_parquet),
                        columns=["gene", "segmentation", "rank_type"],
                        filters=[("experiment", "=", exp_name),
                                  ("rank_type", "=", "bottom")],
                    ).to_pandas()
                    chad_bot_set = {(str(g), int(s)) for g, s
                                      in zip(ct["gene"], ct["segmentation"])}
                    seg_arr = obs.get("pma_segmentation",
                                       obs.get("segmentation")).astype("int64").values
                    in_chad_bot = np.fromiter(
                        ((g, int(s)) in chad_bot_set
                         for g, s in zip(obs_g_str.values, seg_arr)),
                        dtype=bool, count=len(obs),
                    )
                    cell_h = _cell_hashes(
                        np.full(len(obs), exp_name, dtype=object),
                        seg_arr,
                    )
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("uint64").values
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(
                            in_upper & ~in_chad_bot & (cell_h <= thresh),
                            index=obs.index,
                        ))
                    )
                else:  # mixed = combined_rank top-K
                    # Per-cell combined_rank = min(geneKO_rank, chad_rank).
                    # chad_rank from a per-experiment lookup via parquet
                    # predicate-pushdown; cells absent from CHAD's PMA
                    # get chad_rank=MAX, so combined_rank reduces to
                    # geneKO_rank for them.
                    if chad_pma_parquet is None:
                        raise ValueError(
                            "mixed direction requires chad_pma_parquet"
                        )
                    exp_name = p.stem
                    seg_arr = obs.get("pma_segmentation",
                                       obs.get("segmentation"))
                    if seg_arr is None:
                        raise ValueError(
                            "obs missing pma_segmentation/segmentation column"
                        )
                    chad_lookup = _read_chad_rank_for_exp(
                        Path(chad_pma_parquet), exp_name,
                    )
                    max_int64 = np.iinfo(np.int64).max
                    chad_ranks = np.fromiter(
                        (chad_lookup.get((g, int(s)), max_int64)
                         for g, s in zip(obs_g_str.values, seg_arr.values)),
                        dtype=np.int64, count=len(obs),
                    )
                    combined = np.minimum(
                        obs_r.astype("int64").values, chad_ranks,
                    )
                    thresh = obs_g_str.map(
                        lambda g: int(d_thresh.get((g, K_int), 0))
                    ).astype("int64").values
                    if not (thresh > 0).any():
                        # No threshold for this K — bin out of CHAD range.
                        continue
                    rank_mask = (
                        (obs_rt == "top")
                        & (pd.Series(combined <= thresh, index=obs.index))
                    )
            elif rank_type == "mixed_union":
                # UNION top-K: keep cells where geneKO_rank ≤ K OR
                # chad_rank ≤ K. Cell count per gene varies from K (all
                # overlap) to 2K (no overlap). No threshold needed —
                # direct K filter on both ranks.
                if chad_pma_parquet is None:
                    raise ValueError(
                        "mixed_union direction requires chad_pma_parquet"
                    )
                exp_name = p.stem
                seg_arr = obs.get("pma_segmentation",
                                   obs.get("segmentation"))
                chad_lookup = _read_chad_rank_for_exp(
                    Path(chad_pma_parquet), exp_name,
                )
                obs_g_str = obs_g.astype(str)
                max_int64 = np.iinfo(np.int64).max
                chad_ranks = np.fromiter(
                    (chad_lookup.get((g, int(s)), max_int64)
                     for g, s in zip(obs_g_str.values, seg_arr.values)),
                    dtype=np.int64, count=len(obs),
                )
                K_int = int(hi)
                gko_ranks = obs_r.astype("int64").values
                in_either = (gko_ranks <= K_int) | (chad_ranks <= K_int)
                rank_mask = (
                    (obs_rt == "top")
                    & (pd.Series(in_either, index=obs.index))
                )
            elif rank_type == "bottom":
                # Legacy bottom path — used by decay/expand modes against
                # the v3 top+bottom CSV. Inverts PMA rank using
                # gene_max_ranks; not exact-K but matches v3 semantics.
                if gene_max_ranks is None:
                    raise ValueError(
                        "rank_type='bottom' requires gene_max_ranks "
                        "(per-gene max rank from the PMA parquet)."
                    )
                max_r = pd.Series(
                    [int(gene_max_ranks.get(g, 0)) for g in obs_g.values],
                    index=obs.index, dtype="Int64",
                )
                bottom_local = max_r - obs_r + 1
                rank_mask = (
                    (obs_rt == "top")
                    & (bottom_local >= lo)
                    & (bottom_local <= hi)
                )
            else:
                rank_mask = (
                    (obs_rt == rank_type)
                    & (local_rank >= lo)
                    & (local_rank <= hi)
                )
            # Always pass NTC cells through — they're the reference
            # population for normalize_guide_adata(method="ntc"). But
            # cap at `ntc_per_exp` cells per experiment so we don't
            # drown the bin in millions of NTC cells (especially
            # painful for OP features at ~2k dims wide).
            ntc_mask = (obs_rt == "NTC").to_numpy()
            rank_mask_np = rank_mask.to_numpy()
            ntc_pos = np.where(ntc_mask)[0]
            if len(ntc_pos) > ntc_per_exp:
                rng = np.random.default_rng(seed=hash((p.name, lo, hi, rank_type)) & 0xFFFF_FFFF)
                keep_ntc = rng.choice(ntc_pos, ntc_per_exp, replace=False)
                ntc_keep_mask = np.zeros_like(ntc_mask)
                ntc_keep_mask[keep_ntc] = True
            else:
                ntc_keep_mask = ntc_mask
            mask = rank_mask_np | ntc_keep_mask
            if not mask.any():
                continue
            row_idx = np.where(mask)[0]
            obs_sub = obs.iloc[row_idx].reset_index(drop=True)
            # Raw h5py fancy-index — bounded memory.
            with h5py.File(p, "r") as f:
                # AnnData layout: X is either a 2-D dataset (dense) or
                # a group with .data/.indices/.indptr (sparse). cell-dino
                # is dense (1024 PCs).
                X_ds = f["X"]
                if isinstance(X_ds, h5py.Dataset):
                    # Fancy index in sorted order, then unsort to row_idx
                    # order (faster than direct fancy on unsorted ints).
                    order = np.argsort(row_idx)
                    sorted_idx = row_idx[order]
                    sorted_slab = X_ds[sorted_idx, :]
                    slab = np.empty_like(sorted_slab)
                    slab[order] = sorted_slab
                else:
                    # Unexpected sparse layout — fall back to anndata.
                    a = ad.read_h5ad(p)
                    slab = np.asarray(a.X[row_idx, :], dtype=np.float32)
                    del a
                if var_names is None:
                    var_names = [s.decode() if isinstance(s, bytes) else s
                                  for s in f["var"]["_index"][:]]
            X_blocks.append(np.asarray(slab, dtype=np.float32))
            obs_blocks.append(obs_sub)
        except Exception as e:
            print(f"  warn: {p.name} skipped: {e}")
            continue

    if not X_blocks:
        return None
    X_concat = np.vstack(X_blocks)
    obs_concat = pd.concat(obs_blocks, ignore_index=True)
    var_df = pd.DataFrame(index=var_names) if var_names else None
    merged = ad.AnnData(X=X_concat, obs=obs_concat, var=var_df)
    return _score_bin(merged, lo, hi, rank_type, null_size,
                      distrib_csv=distrib_csv, chad_yaml=chad_yaml)


def compute_bin_map(
    adata: ad.AnnData, lo: int, hi: int, rank_type: str,
    rank_offsets: dict,
    null_size: int = 10_000,
    chad_yaml: Optional[Path] = None,
) -> Optional[dict]:
    """Filter to bin → aggregate per gene → EBI consistency mAP.

    `rank_offsets` maps (rank_type, gene) → PMA min-rank offset; we
    subtract to get per-gene local rank 1..5000 within each rank_type.
    """
    obs_rt = adata.obs["rank_type"].astype(str)
    obs_g  = adata.obs.get("pma_gene", adata.obs.get("perturbation")).astype(str)
    obs_r  = pd.to_numeric(adata.obs["rank"], errors="coerce").astype("Int64")
    offset = pd.Series(
        [int(rank_offsets.get((rt, g), 1))
         for rt, g in zip(obs_rt.values, obs_g.values)],
        index=adata.obs.index, dtype="Int64",
    )
    local_rank = obs_r - offset + 1
    if rank_type == "top+bottom":
        top_cap = min(hi, 5000)
        bot_cap = max(0, hi - 5000)
        rank_mask = (
            ((obs_rt == "top") & (local_rank >= 1) & (local_rank <= top_cap))
            | ((obs_rt == "bottom") & (local_rank >= 1) & (local_rank <= bot_cap))
        )
    else:
        rank_mask = (obs_rt == rank_type) & (local_rank >= lo) & (local_rank <= hi)
    # Always include NTC cells (reference for NTC-norm).
    mask = rank_mask | (obs_rt == "NTC")
    sub = adata[mask].copy()
    if sub.n_obs < 50:
        return None
    return _score_bin(sub, lo, hi, rank_type, null_size, chad_yaml=chad_yaml)


def _score_bin(sub: ad.AnnData, lo: int, hi: int, rank_type: str,
               null_size: int,
               distrib_csv: Optional[Path] = None,
               chad_yaml: Optional[Path] = None) -> Optional[dict]:
    from ops_model.features.anndata_utils import (
        aggregate_to_level, normalize_guide_adata,
    )
    from ops_utils.analysis.map_scores import (
        phenotypic_consistency_ebi, phenotypic_distinctivness,
        phenotypic_consistency_manual_annotation,
    )
    if sub.n_obs < 50:
        return None
    # First aggregate to GUIDE level (preserves multiple NTC sgRNAs as
    # separate rows, which `normalize_guide_adata("ntc")` needs for a
    # proper std reference — at gene level NTC collapses to 1 row and
    # zscore_normalize silently falls back to global).
    try:
        guide_a = aggregate_to_level(
            sub, "guide", method="mean", preserve_batch_info=False,
        )
    except Exception as e:
        print(f"  aggregate_to_level(guide) failed: {e}")
        return None
    # NTC z-score at guide level — matches pca_optimization order.
    # Fail loud rather than silently falling back to global z-score
    # (which would invalidate the NTC-normed baseline comparison).
    n_ntc = int((guide_a.obs["perturbation"].astype(str).str.upper()
                  == "NTC").sum())
    if n_ntc < 2:
        raise RuntimeError(
            f"NTC normalization requires ≥2 NTC sgRNA rows at guide level, "
            f"got {n_ntc} for bin {rank_type} {lo}-{hi}. Cache must include "
            f"NTC cells (check _filter_one_experiment + _has_ntc_rows)."
        )
    guide_a = normalize_guide_adata(guide_a, norm_method="ntc")

    # Aggregate guide→gene for consistency mAP (mean across sgRNAs of
    # the same perturbation).
    try:
        gene_a = aggregate_to_level(
            guide_a, "gene", method="mean", preserve_batch_info=False,
        )
    except Exception as e:
        print(f"  aggregate_to_level(gene) failed: {e}")
        return None
    # Drop genes whose feature row is all-NaN. Happens in small bins
    # (e.g. 1/gene, 4/gene) when the 5-px spatial-NN match radius drops
    # the gene's only bin cells — aggregation of an empty group yields
    # NaN features, which then poisons phenotypic_consistency_ebi with
    # "Input contains NaN". Keeping the surviving genes is the right
    # thing: mAP is computed over whatever genes the bin actually has.
    import numpy as np  # local — module already imports it but be explicit
    X = gene_a.X
    nan_mask = np.isnan(X).any(axis=1)
    if hasattr(nan_mask, "A1"):  # sparse
        nan_mask = nan_mask.A1
    n_nan = int(nan_mask.sum())
    if n_nan:
        print(f"  dropping {n_nan} gene rows with NaN features "
              f"(spatial-NN match dropouts at small bin sizes)")
        gene_a = gene_a[~nan_mask].copy()
    if gene_a.n_obs < 3:
        return None
    try:
        map_df, ratio = phenotypic_consistency_ebi(
            gene_a, plot_results=False, null_size=null_size,
            cache_similarity=True,
        )
    except Exception as e:
        print(f"  phenotypic_consistency_ebi failed: {e}")
        return None
    if len(map_df) == 0:
        return None
    # Sidecar: per-complex mAP values for this bin — drives the IQR /
    # violin distribution layer in `plot_decay`. Same stem as the
    # summary CSV with `_complexes` suffix.
    if distrib_csv is not None:
        distrib_csv.parent.mkdir(parents=True, exist_ok=True)
        keep_cols = [c for c in
                     ("complex_id", "complex_num", "mean_average_precision",
                      "corrected_p_value", "below_corrected_p")
                     if c in map_df.columns]
        d = map_df[keep_cols].copy()
        d.insert(0, "rank_hi", int(hi))
        d.insert(0, "rank_lo", int(lo))
        d.insert(0, "rank_type", rank_type)
        d.to_csv(distrib_csv, index=False)
    vals = map_df["mean_average_precision"].astype(float).to_numpy()

    # CHAD complex consistency — same family as EBI but using the
    # `chad_positive_controls_v4.yml` gene clusters as the complex
    # ground truth. User says EBI and CHAD are "pretty close"; we
    # measure both to confirm.
    chad_stats = {}
    if chad_yaml is not None and Path(chad_yaml).exists():
        try:
            cmap_df, cratio = phenotypic_consistency_manual_annotation(
                gene_a, plot_results=False, null_size=null_size,
                cache_similarity=True, annotation_path=str(chad_yaml),
            )
            if len(cmap_df) > 0:
                cvals = cmap_df["mean_average_precision"].astype(float).to_numpy()
                chad_stats = {
                    "n_clusters_chad":     int(len(cmap_df)),
                    "mean_map_chad":       float(cvals.mean()),
                    "std_map_chad":        float(cvals.std(ddof=1)
                                                  if len(cvals) > 1 else 0.0),
                    "median_map_chad":     float(np.median(cvals)),
                    "p25_map_chad":        float(np.percentile(cvals, 25)),
                    "p75_map_chad":        float(np.percentile(cvals, 75)),
                    "chad_consistent_ratio": float(cratio),
                }
        except Exception as e:
            print(f"  phenotypic_consistency_manual_annotation failed: {e}")

    # Distinctiveness (gene-vs-gene mAP) — runs on the same NTC-normed
    # guide-level AnnData built above.
    dist_stats = {}
    try:
        if guide_a.n_obs >= 6:
            d_map_df, d_ratio = phenotypic_distinctivness(
                guide_a, plot_results=False, null_size=null_size,
            )
            if len(d_map_df) > 0:
                dvals = d_map_df["mean_average_precision"].astype(float).to_numpy()
                dist_stats = {
                    "n_perts_distinct":    int(len(d_map_df)),
                    "mean_map_dist":       float(dvals.mean()),
                    "std_map_dist":        float(dvals.std(ddof=1)
                                                  if len(dvals) > 1 else 0.0),
                    "median_map_dist":     float(np.median(dvals)),
                    "p25_map_dist":        float(np.percentile(dvals, 25)),
                    "p75_map_dist":        float(np.percentile(dvals, 75)),
                    "distinctive_ratio":   float(d_ratio),
                }
    except Exception as e:
        print(f"  phenotypic_distinctivness failed: {e}")

    out = {
        "rank_type":        rank_type,
        "rank_lo":          int(lo),
        "rank_hi":          int(hi),
        "n_cells":          int(sub.n_obs),
        "n_genes":          int(gene_a.n_obs),
        "n_complexes":      int(len(map_df)),
        "mean_map":         float(vals.mean()),
        "std_map":          float(vals.std(ddof=1) if len(vals) > 1 else 0.0),
        "sem_map":          float(vals.std(ddof=1) / np.sqrt(len(vals))
                                   if len(vals) > 1 else 0.0),
        "median_map":       float(np.median(vals)),
        "p25_map":          float(np.percentile(vals, 25)),
        "p75_map":          float(np.percentile(vals, 75)),
        "p10_map":          float(np.percentile(vals, 10)),
        "p90_map":          float(np.percentile(vals, 90)),
        "consistent_ratio": float(ratio),
    }
    out.update(chad_stats)
    out.update(dist_stats)
    return out


def _bin_worker(per_exp_dir_s: str, lo: int, hi: int, rank_type: str,
                results_dir_s: str, rank_offsets: dict,
                per_exp_pca_dir_s: Optional[str] = None,
                null_size: int = 10_000,
                source: str = "geneKO",
                chad_yaml_s: Optional[str] = None,
                gene_max_ranks: Optional[dict] = None,
                compacted_thresholds: Optional[dict] = None,
                pca_only: bool = False,
                chad_pma_parquet_s: Optional[str] = None) -> str:
    """Picklable top-level worker for SLURM. Lazily reads the per-
    experiment filtered h5ads, accumulates bin cells, scores mAP,
    writes the result row to `results_dir/bin_<rt>_<lo>_<hi>[_chad].csv`.

    `rank_type` controls the cell-selection strategy:
      - "top"    : top-K cells per gene by attention rank (existing).
      - "bottom" : bottom-K cells per gene by attention rank.
      - "random" : random K cells per gene (control / null baseline).

    `pca_only`: when True, skips the raw 1024-d aggregation entirely and
    only scores against the PCA 97-d cache. ~10× faster (less memory +
    smaller features). Output still has `mean_map_pca` columns; raw
    `mean_map` columns are absent.

    `source` distinguishes the PMA origin; output filename embeds it
    via `_{source}` suffix so multiple sources coexist cleanly.
    """
    results_dir = Path(results_dir_s)
    results_dir.mkdir(parents=True, exist_ok=True)
    suffix = "" if source == "geneKO" else f"_{source}"
    out_csv = results_dir / f"bin_{rank_type}_{lo:05d}_{hi:05d}{suffix}.csv"
    distrib_csv = (results_dir
                   / f"bin_{rank_type}_{lo:05d}_{hi:05d}{suffix}_complexes.csv")
    chad_yaml_p = Path(chad_yaml_s) if chad_yaml_s else None

    if pca_only:
        # Skip raw; only score against the PCA dir. Stub out the raw row
        # so the merged row carries only `_pca` columns (plus the bin
        # identity columns the plotter reads).
        if not per_exp_pca_dir_s or not Path(per_exp_pca_dir_s).is_dir():
            raise ValueError(
                "pca_only=True but per_exp_pca_dir_s missing/invalid"
            )
        pca_distrib_csv = (results_dir
                           / f"bin_{rank_type}_{lo:05d}_{hi:05d}{suffix}_complexes_pca.csv")
        row_pca = compute_bin_map_from_per_exp(
            Path(per_exp_pca_dir_s), lo, hi, rank_type, rank_offsets,
            null_size, distrib_csv=pca_distrib_csv, chad_yaml=chad_yaml_p,
            gene_max_ranks=gene_max_ranks,
            compacted_thresholds=compacted_thresholds,
            chad_pma_parquet=(Path(chad_pma_parquet_s)
                               if chad_pma_parquet_s else None),
        )
        if row_pca is None:
            out_csv.touch()
            return f"empty bin: {rank_type} {lo}-{hi}"
        row = {"rank_type": rank_type, "rank_lo": int(lo), "rank_hi": int(hi),
               "n_cells": row_pca.get("n_cells"),
               "n_genes": row_pca.get("n_genes")}
        for k in ("mean_map", "std_map", "sem_map", "median_map",
                  "p25_map", "p75_map", "p10_map", "p90_map",
                  "n_complexes", "consistent_ratio",
                  "mean_map_chad", "std_map_chad", "median_map_chad",
                  "p25_map_chad", "p75_map_chad",
                  "chad_consistent_ratio", "n_clusters_chad",
                  "mean_map_dist", "std_map_dist", "median_map_dist",
                  "p25_map_dist", "p75_map_dist",
                  "distinctive_ratio", "n_perts_distinct"):
            if k in row_pca:
                row[f"{k}_pca"] = row_pca[k]
        row["source"] = source
        tmp = out_csv.with_suffix(".csv.tmp")
        pd.DataFrame([row]).to_csv(tmp, index=False)
        tmp.replace(out_csv)
        return f"wrote {out_csv} (pca-only)"

    row = compute_bin_map_from_per_exp(
        Path(per_exp_dir_s), lo, hi, rank_type, rank_offsets, null_size,
        distrib_csv=distrib_csv, chad_yaml=chad_yaml_p,
        gene_max_ranks=gene_max_ranks,
        compacted_thresholds=compacted_thresholds,
        chad_pma_parquet=(Path(chad_pma_parquet_s)
                           if chad_pma_parquet_s else None),
    )
    if row is None:
        out_csv.touch()
        return f"empty bin: {rank_type} {lo}-{hi}"

    if per_exp_pca_dir_s and Path(per_exp_pca_dir_s).is_dir():
        pca_distrib_csv = (results_dir
                           / f"bin_{rank_type}_{lo:05d}_{hi:05d}{suffix}_complexes_pca.csv")
        row_pca = compute_bin_map_from_per_exp(
            Path(per_exp_pca_dir_s), lo, hi, rank_type, rank_offsets,
            null_size, distrib_csv=pca_distrib_csv, chad_yaml=chad_yaml_p,
            gene_max_ranks=gene_max_ranks,
            compacted_thresholds=compacted_thresholds,
            chad_pma_parquet=(Path(chad_pma_parquet_s)
                               if chad_pma_parquet_s else None),
        )
        if row_pca is not None:
            for k in ("mean_map", "std_map", "sem_map", "median_map",
                      "p25_map", "p75_map", "p10_map", "p90_map",
                      "n_complexes", "consistent_ratio",
                      "mean_map_chad", "std_map_chad", "median_map_chad",
                      "p25_map_chad", "p75_map_chad",
                      "chad_consistent_ratio", "n_clusters_chad",
                      "mean_map_dist", "std_map_dist", "median_map_dist",
                      "p25_map_dist", "p75_map_dist",
                      "distinctive_ratio", "n_perts_distinct"):
                if k in row_pca:
                    row[f"{k}_pca"] = row_pca[k]

    row["source"] = source
    tmp = out_csv.with_suffix(".csv.tmp")
    pd.DataFrame([row]).to_csv(tmp, index=False)
    tmp.replace(out_csv)
    return f"wrote {out_csv}"


def compute_all_cells_baseline(
    gene_h5ad: Path, out_json: Path, null_size: int = 10_000,
) -> Optional[float]:
    """Run EBI consistency mAP on `gene_pca_optimized.h5ad` (per-gene
    mean over ALL ~60k cells per gene, projected into the same 97-dim
    PCA space). Horizontal reference line on the consistency plot.

    Cached in `out_json` so we don't recompute on every replot.
    """
    import json
    if out_json.exists():
        try:
            return float(json.loads(out_json.read_text())["mean_map"])
        except Exception:
            pass
    if not gene_h5ad.exists():
        print(f"  [baseline] {gene_h5ad} not found — skipping reference line")
        return None
