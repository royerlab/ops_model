def _collect_bin_csvs(results_dir: Path) -> pd.DataFrame:
    """Glob per-bin CSVs from `results_dir` and concat into one frame.

    Skips `*_complexes*.csv` (per-complex distribution sidecars) and
    backfills `source` from the filename (`_chad.csv` → chad, else geneKO).
    """
    rows = []
    for f in sorted(results_dir.glob("bin_*.csv")):
        if "_complexes" in f.name:
            continue
        if f.stat().st_size == 0:
            continue
        try:
            r = pd.read_csv(f)
        except (pd.errors.EmptyDataError, pd.errors.ParserError):
            continue
        if "source" not in r.columns:
            r["source"] = "chad" if f.stem.endswith("_chad") else "geneKO"
        rows.append(r)
    if not rows:
        return pd.DataFrame()
    return pd.concat(rows, ignore_index=True)


# ── Plotting ─────────────────────────────────────────────────────────────
# Color scheme: geneKO PMA = green family, chad PMA = orange family.
# Top attention = bold, bottom attention = muted. Raw 1024-d = solid
# line + circle marker, PCA 97-d = dashed line + square marker.
_SRC_COLORS = {
    ("geneKO", "top"):    "#1F9B4A",
    ("geneKO", "bottom"): "#9CCEA7",
    ("chad",   "top"):    "#D9530B",
    ("chad",   "bottom"): "#F2A88A",
    # Expand-mode top+bottom cumulative window.
    ("geneKO", "top+bottom"): "#5680b8",
    ("chad",   "top+bottom"): "#B57EDC",
    # Expansion-mode (all-cells PMA): fast = single-pass forward, slow =
    # 20-rep averaged (matches trained bag size). Same green family as
    # geneKO since the underlying classifier is the same; darker shade for
    # slow (more reliable attention values).
    ("geneKO_fast", "top"):    "#7FB069",
    ("geneKO_slow", "top"):    "#0F5132",
    # Comparison lines (fast cache only). Bottom-attention = warm red,
    # random sample = neutral grey. Both fall well below the top-attention
    # line if the model is meaningfully ranking.
    ("geneKO_fast", "bottom"):              "#C0392B",
    ("geneKO_fast", "random"):              "#7F8C8D",
    # Mixed-with-CHAD lines: purple family for "mixed = combined-rank",
    # blue family for "mixed_union" (set union). random_low_removed =
    # gold (filtered random — sits between top and random).
    ("geneKO_fast", "mixed"):               "#6E23B8",
    ("geneKO_fast", "mixed_union"):         "#1F46A6",
    ("geneKO_fast", "random_low_removed"):       "#B5800B",
    # Union-low: dark gold — stricter version of RLR that also drops
    # CHAD-bottom cells. Plots above RLR if CHAD's low-attention pruning
    # catches additional bad cells geneKO missed.
    ("geneKO_fast", "random_low_removed_union"): "#6F4E37",
    # Intersection-low: olive green — looser pool than RLR (only removes
    # cells BOTH classifiers call low). Should track random more closely
    # if CHAD's bottom-5k overlaps with geneKO's bottom-half.
    ("geneKO_fast", "random_low_removed_intersection"): "#556B2F",
    # CHAD source's own top-attention curve — orange family to match
    # decay/expand mode's existing CHAD coloring (see "chad" entries).
    ("chad", "top"):                        "#D9530B",
}


def _plot_metric(df: pd.DataFrame, out_path: Path, mode: str,
                  metric: str, ylabel: str, title: str,
                  baseline_consistency: Optional[float] = None,
                  baseline_distinct: Optional[float] = None) -> None:
    """Generic metric-vs-attention-rank line plot.

    `metric` ∈ {"consistency", "distinctiveness"}. Iterates over the
    {source × rank_type} combos present in `df` (rank_type values like
    `top`, `bottom`, `top+bottom`; source values like `geneKO`, `chad`)
    and draws one solid (raw) + one dashed (PCA) line per combo.
    """
    fig, ax = plt.subplots(figsize=(13, 6.5))
    # Expansion + expand both bin cumulatively by per-gene rank cap, so
    # they share the same x-axis field. Only the input data differs
    # (expansion = all-cells PMA, expand = top/bottom-5k v3 PMA).
    x_field = "rank_hi" if (mode == "expand" or mode.startswith("expansion")) else "rank_lo"
    if metric == "consistency":
        raw_col, pca_col = "mean_map", "mean_map_pca"
        baseline = baseline_consistency
        baseline_label = "All cells (PCA, EBI consistency)"
    elif metric == "chad":
        raw_col, pca_col = "mean_map_chad", "mean_map_chad_pca"
        baseline = baseline_consistency  # repurposed: chad baseline passed here
        baseline_label = "All cells (PCA, CHAD consistency)"
    else:
        raw_col, pca_col = "mean_map_dist", "mean_map_dist_pca"
        baseline = baseline_distinct
        baseline_label = "All cells (PCA, distinctiveness)"

    if "source" not in df.columns:
        df = df.copy()
        df["source"] = "geneKO"
    # Drop stale `top+bottom` bins (old expand layout — bottom is now
    # plotted as its own independent curve, not mixed into top).
    df = df[df["rank_type"].astype(str) != "top+bottom"].copy()

    # Truncate each (source, rank_type) curve once population coverage
    # drops below max n_complexes. Done up-front so the y-axis range
    # also reflects only plotted bins (deep-tail bins inflate the
    # distinctiveness mAP and would otherwise blow up ymax).
    #
    # Expansion mode: n_complexes is stable across all bins (~30 EBI /
    # ~25 CHAD) since aggregation always lands at ~1000 genes regardless
    # of cell count, and the all-cells baseline row has NaN n_complexes
    # by construction. Skip truncation entirely so the baseline endpoint
    # isn't dropped.
    def _truncate(sub: pd.DataFrame) -> pd.DataFrame:
        if mode.startswith("expansion"):
            return sub
        if "n_complexes" not in sub.columns or len(sub) <= 1:
            return sub
        full_n = int(sub["n_complexes"].max())
        full_mask = (sub["n_complexes"] == full_n).to_numpy()
        if not full_mask.any():
            return sub
        last_full_pos = int(np.where(full_mask)[0].max())
        if last_full_pos < len(sub) - 1:
            dropped = len(sub) - 1 - last_full_pos
            print(f"  [plot/{metric}] {sub['source'].iat[0]} "
                  f"{sub['rank_type'].iat[0]}: truncated "
                  f"{dropped} tail bins (lost complex coverage "
                  f"below n={full_n})")
        return sub.iloc[: last_full_pos + 1]

    rank_types_present = sorted(df["rank_type"].dropna().astype(str).unique())
    sources_present    = sorted(df["source"].dropna().astype(str).unique())

    truncated_groups = []
    for source in sources_present:
        for rt in rank_types_present:
            sub = df[(df["source"] == source) & (df["rank_type"] == rt)].sort_values(x_field)
            if sub.empty:
                continue
            truncated_groups.append(_truncate(sub))
    df = (pd.concat(truncated_groups, ignore_index=True)
          if truncated_groups else df.iloc[0:0])

    # Per (source, rt) drop count from the random group: for "removed"
    # directions, n_cells diff vs random at the largest shared K shows how
    # many cells the pre-filter dropped. Annotated into the legend label so
    # the reader sees the cost of each filter at a glance.
    drop_counts: dict[tuple, int] = {}
    for source in sources_present:
        rnd = df[(df["source"] == source) & (df["rank_type"] == "random")]
        if rnd.empty:
            continue
        kmax = float(rnd[x_field].max())
        rnd_n = float(rnd[rnd[x_field] == kmax]["n_cells"].iloc[0])
        for rt in rank_types_present:
            if not rt.startswith("random_low_removed"):
                continue
            sub_rt = df[(df["source"] == source)
                        & (df["rank_type"] == rt)
                        & (df[x_field] == kmax)]
            if sub_rt.empty:
                continue
            diff = int(round(rnd_n - float(sub_rt["n_cells"].iloc[0])))
            if diff > 0:
                drop_counts[(source, rt)] = diff

    def _fmt_drop(n: int) -> str:
        if n >= 1_000_000:
            return f"{n/1_000_000:.1f}M"
        if n >= 1_000:
            return f"{n/1_000:.0f}k"
        return str(n)

    for source in sources_present:
        for rt in rank_types_present:
            sub = df[(df["source"] == source) & (df["rank_type"] == rt)].sort_values(x_field)
            if sub.empty:
                continue
            xs = sub[x_field].to_numpy(dtype=float)
            color = _SRC_COLORS.get((source, rt), "#444444")
            base_lbl = f"{source} {rt}"
            if (source, rt) in drop_counts:
                base_lbl = f"{base_lbl} (−{_fmt_drop(drop_counts[(source, rt)])} dropped)"
            if raw_col in sub.columns and sub[raw_col].notna().any():
                ax.plot(xs, sub[raw_col].to_numpy(dtype=float), "o-",
                        color=color, lw=2.2, ms=6,
                        markeredgecolor="black", markeredgewidth=0.4,
                        label=f"{base_lbl} (raw)", zorder=3)
            if pca_col in sub.columns and sub[pca_col].notna().any():
                ax.plot(xs, sub[pca_col].to_numpy(dtype=float), "s--",
                        color=color, lw=1.8, ms=5,
                        markeredgecolor="black", markeredgewidth=0.4,
                        alpha=0.85,
                        label=f"{base_lbl} (PCA)", zorder=4)

    if baseline is not None and np.isfinite(baseline):
        ax.axhline(baseline, color="#d62728", linestyle=":", lw=1.8,
                    alpha=0.85, zorder=2,
                    label=f"{baseline_label} = {baseline:.3f}")

    if (mode == "expand" or mode.startswith("expansion")):
        x_label = "Cells per gene in window (cumulative top-K)"
    else:
        x_label = "Attention rank (bin start)"
    ax.set_xlabel(x_label, fontsize=14)
    ax.set_ylabel(ylabel, fontsize=14)
    ax.set_title(title, fontsize=15, fontweight="bold")
    ax.grid(True, alpha=0.3)
    if (mode == "expand" or mode.startswith("expansion")):
        ax.set_xscale("log")
    ax.legend(fontsize=9, framealpha=0.9, loc="upper left",
               bbox_to_anchor=(1.02, 1.0), borderaxespad=0.0)

    # Annotate the peak of the primary top-attention curve (expansion
    # mode only). Drawn after the lines so it sits on top.
    if mode.startswith("expansion") and metric in ("consistency", "distinctiveness", "chad"):
        col_for_peak = pca_col if pca_col in df.columns else raw_col
        # Pick the top-attention line of the main classifier source.
        main_src = "geneKO_fast" if "geneKO_fast" in df.get("source", pd.Series()).unique() else None
        if main_src and col_for_peak in df.columns:
            sub_peak = df[(df["source"] == main_src)
                          & (df["rank_type"] == "top")
                          & df[col_for_peak].notna()].sort_values(x_field)
            if not sub_peak.empty:
                vals = sub_peak[col_for_peak].to_numpy(dtype=float)
                xs   = sub_peak[x_field].to_numpy(dtype=float)
                i_peak = int(np.argmax(vals))
                ax.scatter([xs[i_peak]], [vals[i_peak]], s=120,
                            facecolor="none", edgecolor="black",
                            linewidth=1.8, zorder=10)
                ax.annotate(
                    f"peak: K={int(xs[i_peak]):,}/gene, mAP={vals[i_peak]:.3f}",
                    xy=(xs[i_peak], vals[i_peak]),
                    xytext=(8, 12), textcoords="offset points",
                    fontsize=10, fontweight="bold", color="black",
                    bbox=dict(boxstyle="round,pad=0.3",
                                facecolor="white", edgecolor="black",
                                alpha=0.9),
                    arrowprops=dict(arrowstyle="-", color="black", lw=0.8),
                    zorder=11,
                )

    cand = []
    for col in (raw_col, pca_col):
        if col in df.columns and df[col].notna().any():
            cand.append(float(df[col].max()))
    if baseline is not None and np.isfinite(baseline):
        cand.append(float(baseline))
    ymax_raw = max(cand) if cand else 0.5
    ymax = max(0.1, np.ceil(ymax_raw * 10) / 10 + 0.05)
    ax.set_ylim(0, ymax)
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, format="pdf", bbox_inches="tight")
    fig.savefig(out_path.with_suffix(".png"), format="png", dpi=200, bbox_inches="tight")
    fig.savefig(out_path.with_suffix(".svg"), format="svg", bbox_inches="tight")
    plt.close(fig)
    print(f"Wrote: {out_path}")


def plot_consistency(df: pd.DataFrame, out_path: Path,
                      mode: str = "decay",
                      baseline: Optional[float] = None) -> None:
    title = ("EBI complex consistency mAP vs attention rank "
              f"({'cumulative top-K' if mode == 'expand' else 'sliding window'})")
    _plot_metric(df, out_path, mode, "consistency",
                 ylabel="EBI complex consistency mAP",
                 title=title, baseline_consistency=baseline)


def plot_chad_consistency(df: pd.DataFrame, out_path: Path,
                           mode: str = "decay",
                           baseline: Optional[float] = None) -> None:
    title = ("CHAD complex consistency mAP vs attention rank "
              f"({'cumulative top-K' if mode == 'expand' else 'sliding window'})")
